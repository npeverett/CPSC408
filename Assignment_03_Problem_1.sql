CREATE TABLE Student(
    StudentID               INTEGER PRIMARY KEY,
    FirstName               TEXT,
    LastName                TEXT,
    GPA                     REAL,
    Major                   TEXT,
    FacultyAdvisor          TEXT,
    Address                 TEXT,
    City                    TEXT,
    State                   TEXT,
    ZipCode                 TEXT,
    MobilePhoneNumber       TEXT,
    isDeleted               INTEGER
);